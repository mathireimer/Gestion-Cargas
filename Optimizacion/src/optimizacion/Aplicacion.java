    private void cargarRemolquesEnTabla() {
        try {
            javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) jTable2.getModel();
            modelo.setRowCount(0); // Limpia la tabla antes de cargar nuevos datos

            java.io.BufferedReader reader = new java.io.BufferedReader(new java.io.FileReader("remolques.txt"));
            String linea;
            while ((linea = reader.readLine()) != null) {
                String[] partes = linea.split(",");
                String id = partes[0].split(":")[1].trim();
                String nombre = partes[1].split(":")[1].trim();
                String volumen = partes[2].split(":")[1].trim();
                String peso = partes[3].split(":")[1].trim();

                modelo.addRow(new Object[]{id, nombre, volumen, peso});
            }
            reader.close();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error al cargar remolques: " + e.getMessage());
        }
    }

    private void actualizarUltimoIdMercaderia() {
        try (BufferedReader reader = new BufferedReader(new FileReader("mercaderias.txt"))) {
            String linea;
            int maxId = 0;
            while ((linea = reader.readLine()) != null) {
                if (linea.startsWith("ID:")) {
                    String[] partes = linea.split(",");
                    String idStr = partes[0].split(":")[1].trim();
                    int id = Integer.parseInt(idStr);
                    if (id > maxId) {
                        maxId = id;
                    }
                }
            }
            idMercaderia = maxId + 1;
        } catch (IOException e) {
            idMercaderia = 1;
        }
    }
    
    public static double calcularConsumoCombustible(double pesoTotal, double distancia) {
    double consumoBasePorKm = 0.3; // litros por km sin carga
    double incrementoPorKg = 0.0001; // incremento por kg por km

    return distancia * (consumoBasePorKm + pesoTotal * incrementoPorKg);
}





