    private void guardarUltimoRemolqueEnArchivo(Remolque r) {
        try (BufferedWriter writer = new BufferedWriter(new FileWriter("remolques.txt", true))) {
            writer.write("ID: " + r.getId()
                    + ", Nombre: " + r.getNombre()
                    + ", Volumen: " + r.getCapacidadVolumen()
                    + ", Peso: " + r.getCapacidadPeso());
            writer.newLine();
        } catch (IOException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "‚ùå Error al guardar archivo: " + ex.getMessage());
        }
    }

